    private void eleccion(){
        if(rbmexico.isSelected()){
            nombre = "Mexico D.F";
        }
        if(rbacapulco.isSelected()){
            nombre = "Acapulco";
        }
        if(rbtanga.isSelected()){
            nombre = "Tangamandapio";
        }
        if(rbcancun.isSelected()){
            nombre = "Cancun";
        }
    }
    
