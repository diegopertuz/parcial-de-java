    private void btnelajivelozActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnelajivelozActionPerformed
          double ahorro, emp1;
    
             eleccion();
            ahorro = Double.parseDouble(txtahorro.getText());
            emp1 = 750*1.3;
            if(ahorro > emp1 ){
               lbEmpresa.setText("usted puede viajar a la ciudad de: "+nombre+" con la Empresa EL AJI VELOZ; Buen viaje");
               }else{
                    lbEmpresa.setText("ahorros insuficientes");
                }
            
    }//GEN-LAST:event_btnelajivelozActionPerformed

